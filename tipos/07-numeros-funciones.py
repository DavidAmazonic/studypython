# Modulo de Math de python
import math

# Redondeo

print(round(1.3))
print(round(1.7))

# Valor Absoluto

print(abs(-10))
print(abs(10))

# Tomar numero arriba

print('ceil',math.ceil(1.1))

# Tomar numero abajo

print(math.floor(1.9999))

# Es un numero?

print(math.isnan(23))

# Elevar 10^3

print(math.pow(10, 3))

# Raiz cuadradada

print(math.sqrt(9))